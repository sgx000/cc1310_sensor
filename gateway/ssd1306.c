/* === Warstwa I2C === */
#if defined(SSD1306_HOST_TEST)
/* Na hoscie I2C jest zaslepione - testujemy tylko font/framebuffer. */
static bool i2c_write(const uint8_t *buf, size_t n){ (void)buf; (void)n; return true; }
#else
static bool i2c_write(const uint8_t *buf, size_t n){
    if(!g_i2c) return false;
    I2C_Transaction t;
    memset(&t, 0, sizeof(t));
    t.slaveAddress = SSD1306_I2C_ADDR;
    t.writeBuf     = (void *)buf;
    t.writeCount   = n;
    t.readBuf      = NULL;
    t.readCount    = 0;
    return I2C_transfer(g_i2c, &t);
}
#endif

/* Pojedyncza komenda (bajt kontrolny 0x00 = komenda). */
static bool cmd(uint8_t c){
    uint8_t b[2] = { 0x00, c };
    return i2c_write(b, 2);
}

/* === API === */
bool ssd1306_init(I2C_Handle handle){
    g_i2c = handle;
    g_on  = false;
    g_invert = false;
    memset(g_fb, 0, sizeof(g_fb));

    static const uint8_t init_seq[] = {
        0xAE,             /* display off */
        0xD5, 0x80,       /* clock divide / osc freq */
        0xA8, 0x1F,       /* multiplex ratio = 31 (dla 32 px) */
        0xD3, 0x00,       /* display offset 0 */
        0x40,             /* start line 0 */
        0x8D, 0x14,       /* charge pump ON */
        0x20, 0x00,       /* memory addressing mode = horizontal */
        0xA1,             /* segment remap (kolumna 127 -> SEG0) */
        0xC8,             /* COM output scan direction: remapped */
        0xDA, 0x02,       /* COM pins: sekwencyjne, dla 128x32 */
        0x81, 0x8F,       /* kontrast */
        0xD9, 0xF1,       /* pre-charge */
        0xDB, 0x40,       /* VCOMH deselect */
        0xA4,             /* output wg RAM (nie "all on") */
        0xA6,             /* normalny (nie odwrocony) */
        0x2E,             /* scroll off */
    };

    g_present = true;
    for(size_t i = 0; i < sizeof(init_seq); i++){
        if(!cmd(init_seq[i])){ g_present = false; break; }
    }
    /* Po init zostawiamy panel WYLACZONY - wlaczy go dopiero BTN2. */
    return g_present;
}

bool ssd1306_present(void){ return g_present; }
bool ssd1306_is_on(void){ return g_on; }

void ssd1306_display_on(void){
    if(g_present && cmd(0xAF)) g_on = true;
}
void ssd1306_display_off(void){
    if(g_present && cmd(0xAE)) g_on = false;
}

void ssd1306_set_invert(bool inv){
    if(g_present && cmd(inv ? 0xA7 : 0xA6)) g_invert = inv;
}
bool ssd1306_is_inverted(void){ return g_invert; }

void ssd1306_clear(void){
    memset(g_fb, 0, sizeof(g_fb));
}

void ssd1306_draw_char(uint8_t col, uint8_t page, char c){
    if(page >= SSD1306_PAGES) return;
    uint8_t idx = (uint8_t)c;
    if(idx < 0x20 || idx > 0x7F) idx = 0x20;     /* poza zakresem -> spacja */
    const uint8_t *gl = FONT5x7[idx - 0x20];
    uint16_t base = (uint16_t)page * SSD1306_WIDTH + col;
    for(uint8_t i = 0; i < 5; i++){
        if(col + i < SSD1306_WIDTH) g_fb[base + i] = gl[i];
    }
    if(col + 5 < SSD1306_WIDTH) g_fb[base + 5] = 0x00;   /* odstep */
}

void ssd1306_draw_string(uint8_t col, uint8_t page, const char *s){
    uint8_t x = col;
    while(*s && x < SSD1306_WIDTH){
        ssd1306_draw_char(x, page, *s++);
        x += SSD1306_CHAR_W;
    }
}

bool ssd1306_flush(void){
    if(!g_present) return false;
    /* Okno: kolumny 0..127, strony 0..3 */
    static const uint8_t win[] = { 0x00, 0x21, 0x00, 0x7F, 0x22, 0x00, 0x03 };
    if(!i2c_write(win, sizeof(win))) return false;

    /* Strumien danych: 4 strony po 128 B, kazda z bajtem kontrolnym 0x40 */
    uint8_t buf[1 + SSD1306_WIDTH];
    buf[0] = 0x40;
    for(uint8_t p = 0; p < SSD1306_PAGES; p++){
        memcpy(&buf[1], &g_fb[p * SSD1306_WIDTH], SSD1306_WIDTH);
        if(!i2c_write(buf, sizeof(buf))) return false;
    }
    return true;
}

const uint8_t *ssd1306_framebuffer(void){ return g_fb; }
